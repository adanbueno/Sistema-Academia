-- CONSULTAS NO BANCO DE DADOS
-- 1) Para cada mês mostrar quanto cada academia gastou no total.

	CREATE OR REPLACE VIEW gasto_salario AS
	SELECT id_academia, SUM(salario) AS totalPag, EXTRACT (MONTH FROM data_pag) AS mes, EXTRACT (YEAR FROM data_pag) AS ano
	FROM sys_academia.pagamento_funcionario NATURAL JOIN 
	     sys_academia.funcionario AS F (id_funcionario)
	GROUP BY id_academia, EXTRACT (MONTH FROM data_pag), EXTRACT (YEAR FROM data_pag);

	CREATE OR REPLACE VIEW gasto_manutencao AS
	SELECT id_academia, SUM(valor) AS totalManu, EXTRACT (MONTH FROM data_manutencao) AS mes, EXTRACT (YEAR FROM data_manutencao) AS ano
	FROM sys_academia.manutencao
	GROUP BY id_academia, EXTRACT (MONTH FROM data_manutencao), EXTRACT (YEAR FROM data_manutencao);

	
	CREATE OR REPLACE VIEW gasto_total_p_mes AS
	SELECT COALESCE(GS.id_academia, GM.id_academia) AS id_academia,
	       SUM(COALESCE(GS.totalPag, 0) + COALESCE(GM.totalManu, 0)) AS total_gasto, 
	       COALESCE(GS.mes, GM.mes) AS mes, 
	       COALESCE(GS.ano, GM.ano) AS ano
	FROM gasto_salario AS GS FULL JOIN
	     gasto_manutencao AS GM ON GS.id_academia = GM.id_academia AND GS.mes = GM.mes
	GROUP BY GS.id_academia, GM.id_academia, GS.mes, GM.mes, GS.ano, GM.ano
	ORDER BY id_academia, mes;


	SELECT *
	FROM gasto_total_p_mes;
		

-- 2) Para cada mês mostrar quanto cada academia ganhou.

	CREATE OR REPLACE VIEW ganho_p_mes AS
	SELECT id_academia, EXTRACT (MONTH FROM data_pag) AS mes, EXTRACT (YEAR FROM data_pag) AS ano, SUM(mensalidade) AS totalRec
	FROM sys_academia.pagamento_cliente NATURAL JOIN 
	     sys_academia.cliente AS C (id_cliente)
	GROUP BY id_academia, EXTRACT (MONTH FROM data_pag), EXTRACT (YEAR FROM data_pag)
	ORDER BY id_academia, mes, ano;


-- 3) Quais academias tem uma maior margem de lucro no mes 05 de 2018? (total que ganha - total que gasta)

	CREATE OR REPLACE VIEW margem_lucro AS
	SELECT COALESCE(GAN.id_academia, GAS.id_academia) AS id_academia, 
	       COALESCE(GAN.mes, GAS.mes) AS mes, 
	       COALESCE(GAN.ano, GAS.ano) AS ano,
	       (COALESCE(totalRec, 0) - COALESCE(total_gasto, 0)) AS margem
	FROM ganho_p_mes AS GAN FULL JOIN 
	     gasto_total_p_mes AS GAS ON 
	     GAN.id_academia = GAS.id_academia AND GAN.mes = GAS.mes AND GAN.ano = GAS.ano
	ORDER BY id_academia, ano, mes;


	SELECT id_academia, margem
	FROM margem_lucro
	WHERE mes = 5 AND ano = 2018 AND margem = (SELECT MAX(margem)
						   						FROM margem_lucro);

	
-- 4) Mostre o nome dos funcionarios que sao os mais frequentes a trabalhar e qual academia eles trabalham

	SELECT DISTINCT nome, id_academia
	FROM sys_academia.funcionario AS F, sys_academia.ponto AS P
	WHERE F.id = P.id_funcionario AND (SELECT COUNT(*)
					   FROM sys_academia.ponto AS P1
					   WHERE P1.id_funcionario = F.id) >= ALL (SELECT COUNT(*)
										   FROM sys_academia.ponto AS P2
										   WHERE P2.id_funcionario <> F.id);
	
