CREATE SCHEMA Sys_academia;

-- CRIANDO TABELAS
CREATE TABLE Sys_academia.academia(
	id SERIAL NOT NULL,
	telefone1 BIGINT NOT NULL,
	telefone2 BIGINT,
	rua VARCHAR,
	num_casa VARCHAR,
	cidade VARCHAR,
	estado VARCHAR,
		PRIMARY KEY (id)
);

CREATE TABLE Sys_academia.equipamento(
	id SERIAL NOT NULL UNIQUE,
	nome VARCHAR,
	tipo VARCHAR,
		PRIMARY KEY(id),
		CONSTRAINT tipo CHECK(tipo = ANY (ARRAY['mmii', 'mmss', 'aeróbico', 'geral']))
);

CREATE TABLE Sys_academia.academia_possui(
	id_academia INTEGER NOT NULL,
	id_maquina INTEGER NOT NULL,
	quantidade INTEGER NOT NULL CHECK(quantidade > 0),
		PRIMARY KEY(id_academia, id_maquina),
		FOREIGN KEY (id_academia) REFERENCES sys_academia.academia(id),
		FOREIGN KEY (id_maquina) REFERENCES sys_academia.equipamento(id)
);

CREATE TABLE Sys_academia.manutencao(
	id SERIAL NOT NULL,
	id_academia INTEGER,
	descricao VARCHAR,
	valor INTEGER,
	data_manutencao DATE,
		PRIMARY KEY (id, id_academia),
		FOREIGN KEY (id_academia) REFERENCES sys_academia.academia(id)
);

CREATE TABLE Sys_academia.funcionario(
	id SERIAL NOT NULL,
	cpf BIGINT NOT NULL UNIQUE,
	nome VARCHAR,
	id_academia INTEGER NOT NULL,
	tipo VARCHAR,
	salario INTEGER NOT NULL,
	telefone1 BIGINT NOT NULL,
	telefone2 BIGINT,
	rua VARCHAR,
	num_casa INTEGER,
	cidade VARCHAR,
		PRIMARY KEY (id),
		FOREIGN KEY (id_academia) REFERENCES sys_academia.academia (id),
		CONSTRAINT tipo CHECK (tipo = ANY(ARRAY['enfermeiro', 'instrutor', 'zelador', 'recepcao']))
);

CREATE TABLE Sys_academia.ponto(
	id_funcionario INTEGER NOT NULL,
	data_ponto DATE,
		PRIMARY KEY(id_funcionario, data_ponto),
		FOREIGN KEY (id_funcionario) REFERENCES sys_academia.funcionario(id)
);

CREATE TABLE Sys_academia.cliente(
	id SERIAL NOT NULL,
	cpf BIGINT NOT NULL UNIQUE,
	nome VARCHAR,
	id_academia INTEGER NOT NULL,
	objetivo VARCHAR,
	mensalidade INTEGER NOT NULL,
	data_prox_pag DATE,
	telefone1 BIGINT,
	dat_nasc DATE,
	rua VARCHAR,
	num_casa INTEGER,
	cidade VARCHAR,
		PRIMARY KEY (id),
		CONSTRAINT mensalidade CHECK (mensalidade >= 50),
		FOREIGN KEY (id_academia) REFERENCES sys_academia.academia(id),
		CONSTRAINT objetivo CHECK (objetivo = ANY(ARRAY['hipertrofia', 'força', 'potência', 'resistencia']))
);

CREATE TABLE Sys_academia.pagamento_cliente(
	id_cliente INTEGER,
	id_academia INTEGER,
	data_pag DATE,
		PRIMARY KEY (id_cliente, id_academia, data_pag),
		FOREIGN KEY (id_cliente) REFERENCES sys_academia.cliente(id),
		FOREIGN KEY (id_academia) REFERENCES sys_academia.academia(id)
);

CREATE TABLE Sys_academia.pagamento_funcionario(
	id_funcionario INTEGER,
	id_academia INTEGER,
	data_pag DATE,
		PRIMARY KEY (id_funcionario, id_academia, data_pag),
		FOREIGN KEY (id_funcionario) REFERENCES sys_academia.funcionario(id),
		FOREIGN KEY (id_academia) REFERENCES sys_academia.academia(id)
);

CREATE TABLE Sys_academia.avaliacao_fisica(
	id SERIAL NOT NULL UNIQUE,
	id_cliente INTEGER NOT NULL,
	id_funcionario INTEGER NOT NULL,
	data_feito DATE,
	data_retorno DATE,
	altura FLOAT,
	massa FLOAT,
	massa_magra FLOAT,
	massa_gorda FLOAT,
	observacao VARCHAR,
		PRIMARY KEY (id_cliente, data_feito),
		CONSTRAINT data_retorno CHECK (data_retorno > data_feito),
		FOREIGN KEY (id_cliente) REFERENCES sys_academia.cliente(id),
		FOREIGN KEY (id_funcionario) REFERENCES sys_academia.funcionario(id)
);

CREATE TABLE Sys_academia.planilha_treino(
	id SERIAL NOT NULL UNIQUE,
	id_funcionario INTEGER NOT NULL,
	id_avaliacao INTEGER NOT NULL,
	data_feito DATE,
	intensidade VARCHAR,
		PRIMARY KEY (id_funcionario, id_avaliacao),
		FOREIGN KEY (id_funcionario) REFERENCES sys_academia.funcionario(id),
		FOREIGN KEY (id_avaliacao) REFERENCES sys_academia.avaliacao_fisica(id),
		CONSTRAINT intensidade CHECK (intensidade = ANY(ARRAY['baixa', 'media', 'alta']))
);

CREATE TABLE Sys_academia.info_treino(
	id SERIAL NOT NULL,
	id_treino INTEGER NOT NULL,
	id_maquina INTEGER NOT NULL,
	id_academia INTEGER NOT NULL,
	serie INTEGER NOT NULL,
	repeticoes INTEGER,
	observacao VARCHAR,
	PRIMARY KEY (id),
	FOREIGN KEY (id_treino) REFERENCES sys_academia.planilha_treino(id),
	FOREIGN KEY (id_maquina, id_academia) REFERENCES sys_academia.academia_possui(id_maquina, id_academia)
);

CREATE TABLE Sys_academia.realiza_treino(
	id_cliente INTEGER NOT NULL,
	id_treino INTEGER NOT NULL,
		PRIMARY KEY (id_cliente, id_treino),
		FOREIGN KEY (id_cliente) REFERENCES sys_academia.cliente (id),
		FOREIGN KEY (id_treino) REFERENCES sys_academia.planilha_treino (id)
);

-- MANTENDO INTEGRIDADE DOS DADOS

-- 1) Inserir apenas avaliações feitas apenas pelos enfermeiros
CREATE OR REPLACE FUNCTION insert_avaliacao() RETURNS TRIGGER AS $$
BEGIN
	IF (SELECT 1 FROM sys_academia.funcionario WHERE id = new.id and tipo = 'enfermeiro') = 0
	THEN RAISE EXCEPTION 'AVALIAÇÃO FÍSICA SÓ PODE SER FEITA COM ENFERMEIRO';
	RETURN NULL;
	END IF;
	RETURN new;
END;
$$ language plpgsql;

CREATE TRIGGER insert_avaliacao BEFORE INSERT ON sys_academia.avaliacao_fisica
FOR EACH ROW EXECUTE PROCEDURE insert_avaliacao();


-- 2) Inserir treino feito apenas por instrutor
CREATE OR REPLACE FUNCTION insert_treino() RETURNS TRIGGER AS $$
BEGIN
	IF (SELECT 1 FROM sys_academia.funcionario WHERE id = new.id AND tipo = 'instrutor') =0
	THEN RAISE EXCEPTION 'AVALIAÇÃO FÍSICA SÓ PODE SER FEITA COM INSTRUTOR';
	RETURN NULL;
	END IF;
	RETURN new;
END;
$$ language plpgsql;

CREATE TRIGGER insert_treino BEFORE INSERT ON sys_academia.info_treino
FOR EACH ROW EXECUTE PROCEDURE insert_treino();


